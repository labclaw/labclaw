def _make_llm(*responses: str) -> MagicMock:
    mock = MagicMock()
    mock.complete = AsyncMock(side_effect=list(responses))
    return mock


def _make_tool_returning(name: str, return_value: Any) -> AgentTool:
    """Create an AgentTool whose execute() returns the given value directly."""

    async def fn(**kwargs: Any) -> Any:
        return return_value

    return AgentTool(
        name=name,
        description=f"Tool {name}",
        fn=fn,
        parameters_schema={},
    )


